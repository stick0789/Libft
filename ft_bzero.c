#include "libft.h"

void	ft_bzero(void *b, size_t n)
{
	unsigned char	*p;
	size_t			i;

	p = (unsigned char *)b;
	i = 0;
	while (i < n)
	{
		p[i] = '\0';
		i++;
	}
}
